    private void buttonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOkActionPerformed
        
        String nome = this.produto.getNome();
        String tipo = this.produto.getTipo();
        float preco = this.produto.getPreco();
        int quantidade = this.produto.getQuantidade();
        
        try {
            Produto produto = ControllerProduto.getProduto(nome, preco, tipo, quantidade);
            
            ControllerProduto.update(produto.getId(), "nome", this.textNome.getText());
            ControllerProduto.update(produto.getId(), "tipo", this.textTipo.getText());
            ControllerProduto.update(produto.getId(), "preco", this.textPreco.getText());
            ControllerProduto.update(produto.getId(), "quantidade", this.spinnerQuantidade.getValue().toString());
            ControllerProduto.update(produto.getId(), "status", this.textStatus.getText());
            
            this.labelErro.setText("Editado com sucesso!");
            this.labelErro.setForeground(new Color(8, 163, 0));
            this.labelErro.setVisible(true);
           
        } catch (SQLException ex) {
            Logger.getLogger(ViewProduto.class.getName()).log(Level.SEVERE, null, ex);
            this.labelErro.setText("Erro");
            this.labelErro.setForeground(Color.red);
            this.labelErro.setVisible(true);
        }
    }//GEN-LAST:event_buttonOkActionPerformed

